# Алгоритм проверки, является ли строка палиндромом:
# 1. Привести строку к единому регистру (например, нижнему), чтобы сравнение было нечувствительно к регистру.
# 2. Удалить из строки пробелы и, по желанию, знаки препинания (если хотим обрабатывать фразы).
# 3. Сравнить строку с её перевёрнутым вариантом.
# 4. Если они совпадают — строка является палиндромом.


def is_palindrome(text):
    # Приводим к нижнему регистру
    text = text.lower()

    # Удаляем пробелы и знаки препинания
    cleaned_text = ''.join(char for char in text if char.isalnum())

    # Сравниваем строку с её перевёрнутой версией
    return cleaned_text == cleaned_text[::-1]

print(is_palindrome("А роза упала на лапу Азора"))     # True
print(is_palindrome("123321"))                         # True
print(is_palindrome("Не палиндром"))                   # False